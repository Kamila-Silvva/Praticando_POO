package ExercicioPOO.Areas;

public class ConversaoDeUnidadesDeArea {

    public static double metrosQuadrado (double metros) {
        return metros * 10.76;
    }

    public static double centrimetrosQuadrados (double centimetros) {
        return centimetros * 929;
    }

    public static double milhasQuadrados (double milhas) {
        return milhas * 640;
    }

    public static double acresQuadrados (double acres) {
        return acres * 43.560;
    }
}
